// ── HTML generation ──────────────────────────────────────────

function blockToHtml(block: EmailBlock): string {
  const p = block.props;
  switch (block.type) {
    case "header":
      return `<h2 style="font-family:Arial,sans-serif;font-size:${p.fontSize}px;color:${p.color};text-align:${p.align};margin:0 0 16px 0;">${p.text}</h2>`;
    case "text":
      return `<p style="font-family:Arial,sans-serif;font-size:${p.fontSize}px;color:${p.color};text-align:${p.align};line-height:1.6;margin:0 0 16px 0;">${p.text}</p>`;
    case "image":
      return `<div style="text-align:center;margin:0 0 16px 0;"><img src="${p.src}" alt="${p.alt}" style="max-width:${p.width}%;height:auto;display:inline-block;border-radius:4px;" /></div>`;
    case "button":
      return `<div style="text-align:${p.align};margin:0 0 16px 0;"><a href="${p.url}" style="display:inline-block;padding:14px 32px;background-color:${p.bgColor};color:${p.textColor};text-decoration:none;font-family:Arial,sans-serif;font-size:16px;font-weight:bold;border-radius:${p.borderRadius}px;">${p.text}</a></div>`;
    case "divider":
      return `<hr style="border:none;border-top:${p.thickness}px solid ${p.color};margin:${p.spacing}px 0;" />`;
    default:
      return "";
  }
}

export function blocksToHtml(blocks: EmailBlock[], bgColor = "#1a1a2e"): string {
  const inner = blocks.map(blockToHtml).join("\n  ");
  return `<div style="font-family:Arial,sans-serif;max-width:600px;margin:0 auto;padding:20px;background:${bgColor};color:#ffffff;">\n  ${inner}\n</div>`;
}

