// ── Image Block Settings (separate component to use hooks) ───

function ImageBlockSettings({
  block,
  onChange,
}: {
  block: EmailBlock;
  onChange: (props: Record<string, string>) => void;
}) {
  const [uploading, setUploading] = useState(false);
  const fileInputRef = useRef<HTMLInputElement>(null);
  const update = (key: string, value: string) =>
    onChange({ ...block.props, [key]: value });

  const inputCls =
    "w-full rounded-md border border-input bg-muted/50 px-3 py-1.5 text-sm text-foreground focus:border-primary focus:outline-none focus:ring-1 focus:ring-primary";
  const labelCls = "text-[11px] font-semibold text-muted-foreground uppercase tracking-wider";

  const handleUpload = async (e: React.ChangeEvent<HTMLInputElement>) => {
    const file = e.target.files?.[0];
    if (!file) return;
    if (!file.type.startsWith("image/")) {
      toast({ title: "Please select an image file", variant: "destructive" });
      return;
    }
    if (file.size > 5 * 1024 * 1024) {
      toast({ title: "Image must be under 5MB", variant: "destructive" });
      return;
    }
    setUploading(true);
    const ext = file.name.split(".").pop() || "png";
    const path = `email-images/${crypto.randomUUID()}.${ext}`;
    const { error } = await supabase.storage.from("brand-assets").upload(path, file);
    if (error) {
      toast({ title: "Upload failed", description: error.message, variant: "destructive" });
      setUploading(false);
      return;
    }
    const { data: urlData } = supabase.storage.from("brand-assets").getPublicUrl(path);
    update("src", urlData.publicUrl);
    toast({ title: "Image uploaded!" });
    setUploading(false);
    if (fileInputRef.current) fileInputRef.current.value = "";
  };

  return (
    <div className="space-y-2">
      <div>
        <label className={labelCls}>Image</label>
        <div className="flex gap-2">
          <input
            value={block.props.src}
            onChange={(e) => update("src", e.target.value)}
            placeholder="https://..."
            className={inputCls + " flex-1"}
          />
          <button
            type="button"
            onClick={() => fileInputRef.current?.click()}
            disabled={uploading}
            className="flex items-center gap-1.5 rounded-md border border-input bg-muted/50 px-3 py-1.5 text-xs font-medium text-muted-foreground hover:text-foreground hover:bg-primary/10 transition-colors disabled:opacity-50"
          >
            {uploading ? <Loader2 className="h-3.5 w-3.5 animate-spin" /> : <Upload className="h-3.5 w-3.5" />}
            {uploading ? "..." : "Upload"}
          </button>
          <input
            ref={fileInputRef}
            type="file"
            accept="image/*"
            onChange={handleUpload}
            className="hidden"
          />
        </div>
      </div>
      <div className="grid grid-cols-2 gap-2">
        <div>
          <label className={labelCls}>Alt Text</label>
          <input
            value={block.props.alt}
            onChange={(e) => update("alt", e.target.value)}
            className={inputCls}
          />
        </div>
        <div>
          <label className={labelCls}>Width %</label>
          <input
            type="number"
            min="10"
            max="100"
            value={block.props.width}
            onChange={(e) => update("width", e.target.value)}
            className={inputCls}
          />
        </div>
      </div>
    </div>
  );
}

